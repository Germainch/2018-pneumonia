"""
Dataset 2 — RSNA 2018 Pneumonia Detection Challenge
Exploration summary
=====================================================

Source: https://pubs.rsna.org/doi/10.1148/ryai.2019180041
        Shih G, et al. "Augmenting the National Institutes of Health Chest
        Radiograph Dataset with Expert Annotations of Possible Pneumonia"

== DATASET OVERVIEW ==

  Origin        30,000 frontal-view chest X-rays drawn from the NIH CXR8 dataset
                (112,000 images total). Subset selected for pneumonia annotation.
  Modality      Computed Radiography (CR), DICOM format
  Resolution    1024 × 1024 pixels, 8-bit MONOCHROME2
  Views         16,248 posteroanterior (PA) + 13,752 anteroposterior (AP)
  Annotators    18 radiologists from 16 institutions
                (12 chest radiologists from STR, mean 10.6 years experience)
  Annotation    Manual bounding boxes via md.ai platform

== FILES ==

  pneumonia-challenge-annotations-adjudicated-kaggle_2018.json
      Adjudicated consensus labels (Kaggle challenge ground truth).
      30,000 unique SOPInstanceUIDs / images.
      5 diagnostic label types:
        - Lung Opacity (High Prob)     → sick
        - Lung Opacity (Med Prob)      → sick
        - Lung Opacity (Low Prob)      → sick
        - No Lung Opacity / Not Normal → not sick (abnormal but not pneumonia)
        - Normal                       → not sick
      + admin labels: Question, Exclude, Adjudicate, QA, Flag 1-5, Question Addressed
      Includes bounding box coordinates (x, y, width, height) for opacity regions.
      Total annotations: 83,809  (multiple annotators per image)

  pneumonia-challenge-annotations-original_2018.json
      Original binary labels before adjudication.
      25,684 images (strict subset of adjudicated set).
      2 label types only: "Lung Opacity" / "No Lung Opacity"
      Total annotations: 28,989

  pneumonia-challenge-dataset-mappings_2018.json
      Maps each image to:
        - img_id          : original NIH CXR8 PNG filename (e.g. "00012773_000.png")
        - subset_img_id   : challenge-internal UUID
        - subset_group    : annotation group index
        - StudyInstanceUID, SeriesInstanceUID, SOPInstanceUID : DICOM UIDs
        - studyId, seriesId, instanceId : md.ai platform IDs
        - orig_labels     : NIH CXR8 disease tags (e.g. ["Pneumonia", "Effusion"])
      30,000 entries.

  DICOM files  (mdai_rsna_project_x9N20BZa_images_2018-07-20-153330/)
      Organised as  StudyInstanceUID / SeriesInstanceUID / SOPInstanceUID.dcm
      30,000 files.  Patient metadata per file:
        - PatientID       (UUID string)
        - PatientSex      M / F
        - PatientAge      integer years
        - ViewPosition    PA / AP
        - Modality        CR

== LABEL DISTRIBUTION (adjudicated, majority vote) ==

  Sick (pneumonia / lung opacity)  :  9,247  (30.9 %)
  Not-sick (normal / no opacity)   : 20,669  (69.1 %)
  Skipped (only admin annotations) :     84
  Total labeled                    : 29,916

  Agreement with original binary JSON (25,684 images with both labels):
    The adjudicated and original labels are used independently;
    the adjudicated set is considered the authoritative ground truth.

== CHALLENGE LABEL ENCODING (PDE339-Pneumonia Detection) ==

  0 = Unknown
  1 = Pneumonia present  → sick
  2 = Pneumonia absent   → not_sick

== OUTPUT: labels.csv ==

  Columns:
    img_id              – original NIH CXR8 PNG filename
    SOPInstanceUID      – DICOM SOP UID (primary key)
    StudyInstanceUID    – DICOM Study UID
    SeriesInstanceUID   – DICOM Series UID
    dicom_path          – absolute path to .dcm file
    patient_id          – DICOM PatientID
    patient_sex         – M / F
    patient_age         – integer years
    view_position       – PA or AP
    label               – 1 (sick) or 0 (not_sick)
    label_name          – "sick" or "not_sick"
    label_original      – 1/0 from original binary JSON, empty if not available
    nih_orig_labels     – comma-separated NIH CXR8 disease tags
    bboxes              – JSON list of {x, y, width, height} bounding boxes
                          for opacity regions (empty list for not_sick)

  Generated by: label_images.py
"""

if __name__ == "__main__":
    import json
    import os
    import pandas as pd
    from collections import Counter

    DATASET_DIR = os.path.dirname(os.path.abspath(__file__))
    labels_csv  = os.path.join(DATASET_DIR, "../data/labels.csv")

    df = pd.read_csv(labels_csv)

    print("=" * 55)
    print("RSNA 2018 Pneumonia Detection — Dataset Summary")
    print("=" * 55)

    print(f"\nTotal images      : {len(df)}")
    print(f"Sick (label=1)    : {(df.label == 1).sum()} ({(df.label == 1).mean():.1%})")
    print(f"Not-sick (label=0): {(df.label == 0).sum()} ({(df.label == 0).mean():.1%})")

    print("\n-- Patient sex --")
    print(df["patient_sex"].value_counts().to_string())

    print("\n-- View position --")
    print(df["view_position"].value_counts().to_string())

    print("\n-- Patient age --")
    ages = pd.to_numeric(df["patient_age"], errors="coerce")
    print(f"  mean={ages.mean():.1f}  median={ages.median():.0f}  "
          f"min={ages.min():.0f}  max={ages.max():.0f}")

    print("\n-- Sick by view position --")
    print(df.groupby("view_position")["label"].mean().apply(lambda x: f"{x:.1%}").to_string())

    print("\n-- Top NIH original labels (sick images) --")
    sick_nih = df[df.label == 1]["nih_orig_labels"].dropna()
    tag_counts = Counter(
        tag.strip()
        for row in sick_nih
        for tag in row.split(",")
        if tag.strip()
    )
    for tag, count in tag_counts.most_common(10):
        print(f"  {count:5d}  {tag}")

    print("\n-- Images with bounding boxes --")
    bbox_count = df["bboxes"].apply(lambda x: x != "[]").sum()
    print(f"  {bbox_count} images have at least one opacity bounding box")
